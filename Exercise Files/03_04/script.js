var a = 5;
var b = 4;
var sum = a + b;

alert("sum value: " + sum);

// use one string and one number, the + operator will put them side by side.
b = "4"
sum = a + b;
alert("string sum value:  " + sum);

// when use one number and one string the division become a number.
sum = a / b;
alert("divide by string, numeric sum:  " + sum);

sum = a * b;
alert("multiply by string, numeric sum:  " + sum);

